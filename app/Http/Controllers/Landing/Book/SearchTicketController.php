        {
            $data = array(
                'title' => 'Search Ticket',
                'ticket' => TicketBooking::where('email', $request->mail_or_phone)->orWhere('phone',
                $request->mail_or_phone)->first()
            );

            return view('pages.landing.search.index', $data);
        } else {
            $data = array(
                'title' => 'Search Ticket',
            );
            return view('pages.landing.search.index', $data);
